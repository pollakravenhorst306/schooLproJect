// Node.js example: A simple calculator program that performs addition

// Function to add two numbers
function sum(a, b) {
  return a + b;
}

// Main function to test the sum function with some sample numbers
const num1 = 4.5;
const num2 = 6.7;

console.log(`The result of adding ${num1} and ${num2} is: ${sum(num1, num2)}`);
